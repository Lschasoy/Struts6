package modelos;

/**
 *
 * @author alumno
 */
public class Equipos {
    
    String equipo_cod, nombre, fundacion, presidente, presupuesto, equipacion, foto_equipo, foto_escudo, paginaweb;

    public String getEquipo_cod() {
        return equipo_cod;
    }

    public void setEquipo_cod(String equipo_cod) {
        this.equipo_cod = equipo_cod;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFundacion() {
        return fundacion;
    }

    public void setFundacion(String fundacion) {
        this.fundacion = fundacion;
    }

    public String getPresidente() {
        return presidente;
    }

    public void setPresidente(String presidente) {
        this.presidente = presidente;
    }

    public String getPresupuesto() {
        return presupuesto;
    }

    public void setPresupuesto(String presupuesto) {
        this.presupuesto = presupuesto;
    }

    public String getEquipacion() {
        return equipacion;
    }

    public void setEquipacion(String equipacion) {
        this.equipacion = equipacion;
    }

    public String getFoto_equipo() {
        return foto_equipo;
    }

    public void setFoto_equipo(String foto_equipo) {
        this.foto_equipo = foto_equipo;
    }

    public String getFoto_escudo() {
        return foto_escudo;
    }

    public void setFoto_escudo(String foto_escudo) {
        this.foto_escudo = foto_escudo;
    }

    public String getPaginaweb() {
        return paginaweb;
    }

    public void setPaginaweb(String paginaweb) {
        this.paginaweb = paginaweb;
    }        
}
